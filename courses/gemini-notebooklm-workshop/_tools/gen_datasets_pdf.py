title = ParagraphStyle('t', fontName=F, fontSize=16, leading=24, alignment=1, spaceAfter=4)
sub = ParagraphStyle('s', fontName=F, fontSize=9.5, leading=15, alignment=1, textColor=colors.HexColor('#666'), spaceAfter=12)
h = ParagraphStyle('h', fontName=F, fontSize=12, leading=18, spaceBefore=10, spaceAfter=3, textColor=colors.HexColor('#1a1a1a'))
body = ParagraphStyle('b', fontName=F, fontSize=10.5, leading=18, spaceAfter=3)
bullet = ParagraphStyle('bu', fontName=F, fontSize=10.5, leading=18, spaceAfter=3, leftIndent=12)
note = ParagraphStyle('n', fontName=F, fontSize=9, leading=14, textColor=colors.HexColor('#888'), spaceBefore=8)

NOTE_TEXT = "（弄一下工作室課程練習用虛構資料，非真實公司文件）"
ACCENT = colors.HexColor('#2c5f8a')
GRID = colors.HexColor('#cccccc')
HEADBG = colors.HexColor('#eef2f6')


def doc(fn):
    return SimpleDocTemplate(os.path.join(OUT, fn), pagesize=A4,
                             topMargin=20 * mm, bottomMargin=18 * mm,
                             leftMargin=22 * mm, rightMargin=22 * mm,
                             title=fn.replace('.pdf', ''))


def head(t, s):
    return [Paragraph(t, title), Paragraph(s, sub)]


def tbl(data, widths, header=True):
    t = Table(data, colWidths=widths, repeatRows=1 if header else 0)
    cmds = [
        ('FONTNAME', (0, 0), (-1, -1), F),
        ('FONTSIZE', (0, 0), (-1, -1), 9.5),
        ('GRID', (0, 0), (-1, -1), 0.5, GRID),
        ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
        ('TOPPADDING', (0, 0), (-1, -1), 4),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 4),
        ('LEFTPADDING', (0, 0), (-1, -1), 6),
        ('RIGHTPADDING', (0, 0), (-1, -1), 6),
    ]
    if header:
        cmds += [
            ('BACKGROUND', (0, 0), (-1, 0), HEADBG),
            ('TEXTCOLOR', (0, 0), (-1, 0), ACCENT),
        ]
    t.setStyle(TableStyle(cmds))
    return t


def P(text):
    return Paragraph(text, body)


def B(text):
    return Paragraph('・' + text, bullet)


def end():
    return [Spacer(1, 6 * mm), Paragraph(NOTE_TEXT, note)]


